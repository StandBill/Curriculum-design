package model;

public class Semester {
	private String semid;
	private String semname;
	public String getSemid() {
		return semid;
	}
	public void setSemid(String semid) {
		this.semid = semid;
	}
	public String getSemname() {
		return semname;
	}
	public void setSemname(String semname) {
		this.semname = semname;
	}
	
}
